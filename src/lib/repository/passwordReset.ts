export async function resetAssociatePassword(
  authUid: string,
  newPassword: string
): Promise<boolean> {
  const { error } = await (supabase.auth as any).admin.updateUserById(authUid, {
    password: newPassword,
  })

  if (error) {
    console.error("resetAssociatePassword failed:", error.message)
    return false
  }
  return true
}

